package com.pw.codeset.databean;

public class RecordBean {
    String bookId;
    int chapterPos;
    int startCharPos;

    public String getBookId() {
        return bookId;
    }

    public void setBookId(String bookId) {
        this.bookId = bookId;
    }

    public int getChapterPos() {
        return chapterPos;
    }

    public void setChapterPos(int chapterPos) {
        this.chapterPos = chapterPos;
    }

    public int getStartCharPos() {
        return startCharPos;
    }

    public void setStartCharPos(int startCharPos) {
        this.startCharPos = startCharPos;
    }
}
